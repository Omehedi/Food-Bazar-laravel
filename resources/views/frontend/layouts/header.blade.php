<script>
    function setLanguage(language) {
        const translations = {
            en: {
                home: "Home",
                category: "Category",
                singleNews: "Single News",
                contact: "Contact",
                dropdown: "Dropdown",
                menuItem1: "Menu item 1",
                menuItem2: "Menu item 2",
                menuItem3: "Menu item 3",
                language: "Language",
                date: "Monday, January 1, 2045",
                advertise: "Advertise",
                login: "Login",
                siteTitle: "BangladeshNews",
                mobileTitle: "BangladeshNews",

            },
            bn: {
                home: "হোম",
                category: "বিভাগ",
                singleNews: "একক সংবাদ",
                contact: "যোগাযোগ",
                dropdown: "মেনু",
                menuItem1: "মেনু আইটেম ১",
                menuItem2: "মেনু আইটেম ২",
                menuItem3: "মেনু আইটেম ৩",
                language: "ভাষা",
                date: "সোমবার, ১ জানুয়ারি, ২০৪৫",
                advertise: "বিজ্ঞাপন",
                login: "লগইন",
                siteTitle: "প্রথম আলোNews",
                mobileTitle: "প্রথম আলোNews"

            }
        };

        const lang = translations[language];
        document.querySelectorAll('[data-translate]').forEach(el => {
            const key = el.getAttribute('data-translate');
            if (lang[key]) {
                el.innerText = lang[key];
            }
        });
    }

    document.addEventListener('DOMContentLoaded', () => {
        // Set default language
        setLanguage('en'); // or 'bn' for Bangla
    });
</script>

